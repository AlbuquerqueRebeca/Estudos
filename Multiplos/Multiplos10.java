package Multiplos;

import java.util.Scanner;

public class Multiplos10{
    
    public static void main(String[] args) {
        
    Scanner scanner = new Scanner(System.in);

    int N = scanner.nextInt();
    
    for(int x = 1; x < 10; x ++){ 
        
        System.out.println(N + " x " + x + " = " + N * x);

    }










    }





}